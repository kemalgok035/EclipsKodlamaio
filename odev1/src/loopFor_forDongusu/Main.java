package loopFor_forDongusu;

public class Main {

	public static void main(String[] args) {
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
		System.out.println("For Döngüsü Bitti...");
		
		for(int i=1; i<=10; i+=2) {
			System.out.println(i);
		}
		System.out.println("Tek Sayıları Yazan For Döngüsü Bitti...");
		
		for(int i=2; i<=10; i+=2) {
			System.out.println(i);
		}
		System.out.println("Çift Sayıları Yazan For Döngüsü Bitti...");


	}

}
