package loopWhile_whileDongusu;

public class Main {

	public static void main(String[] args) {
		
		int i = 1;
		
		while(i<=10) {
			
			System.out.println(i);
			i++;
			
		}
		System.out.println("While Döngüsü Bitti...");
		
		
		i = 1;
	while(i<=10) {
			
			System.out.println(i);
			i+=2;
			
		}
		System.out.println("Tek Sayıları Yazan While Döngüsü Bitti...");
		
		i = 2;
		while(i<=10) {
				
				System.out.println(i);
				i+=2;
				
			}
			System.out.println("Çift Sayıları Yazan While Döngüsü Bitti...");

	}

}
